% args: {n=(double,[1, 1],{REAL})}
function  [Fx, Fy, Fz, Vx, Vy, Vz] = nbody1d(n)
  mc_t39 = .5;                        % mc_t39=(double,0.5,[1, 1],{REAL})
  mc_t40 = 0.0833;                    % mc_t40=(double,0.0833,[1, 1],{REAL})
  [dT] = mtimes(mc_t39, mc_t40);      % dT=(double,[1, 1],{REAL})
  T = 5;                              % T=(double,5.0,[1, 1],{REAL})
  mc_t41 = 1;                         % mc_t41=(double,1.0,[1, 1],{REAL})
  [mc_t3] = rand(n, mc_t41);          % mc_t3=(double,[?, 1],{REAL})
  mc_t42 = 1000.23;                   % mc_t42=(double,1000.23,[1, 1],{REAL})
  [Rx] = mtimes(mc_t3, mc_t42);       % Rx=(double,[?, 1],{REAL})
  mc_t43 = 1;                         % mc_t43=(double,1.0,[1, 1],{REAL})
  [mc_t4] = rand(n, mc_t43);          % mc_t4=(double,[?, 1],{REAL})
  mc_t44 = 1000.23;                   % mc_t44=(double,1000.23,[1, 1],{REAL})
  [Ry] = mtimes(mc_t4, mc_t44);       % Ry=(double,[?, 1],{REAL})
  mc_t45 = 1;                         % mc_t45=(double,1.0,[1, 1],{REAL})
  [mc_t5] = rand(n, mc_t45);          % mc_t5=(double,[?, 1],{REAL})
  mc_t46 = 1000.23;                   % mc_t46=(double,1000.23,[1, 1],{REAL})
  [Rz] = mtimes(mc_t5, mc_t46);       % Rz=(double,[?, 1],{REAL})
  mc_t47 = 1;                         % mc_t47=(double,1.0,[1, 1],{REAL})
  [mc_t6] = rand(n, mc_t47);          % mc_t6=(double,[?, 1],{REAL})
  mc_t48 = 345;                       % mc_t48=(double,345.0,[1, 1],{REAL})
  [m] = mtimes(mc_t6, mc_t48);        % m=(double,[?, 1],{REAL})
  mc_t49 = 1;                         % mc_t49=(double,1.0,[1, 1],{REAL})
  [Fx] = zeros(n, mc_t49);            % Fx=(double,[?, 1],{REAL})
  mc_t50 = 1;                         % mc_t50=(double,1.0,[1, 1],{REAL})
  [Fy] = zeros(n, mc_t50);            % Fy=(double,[?, 1],{REAL})
  mc_t51 = 1;                         % mc_t51=(double,1.0,[1, 1],{REAL})
  [Fz] = zeros(n, mc_t51);            % Fz=(double,[?, 1],{REAL})
  mc_t52 = 1;                         % mc_t52=(double,1.0,[1, 1],{REAL})
  [Vx] = zeros(n, mc_t52);            % Vx=(double,[?, 1],{REAL})
  mc_t53 = 1;                         % mc_t53=(double,1.0,[1, 1],{REAL})
  [Vy] = zeros(n, mc_t53);            % Vy=(double,[?, 1],{REAL})
  mc_t54 = 1;                         % mc_t54=(double,1.0,[1, 1],{REAL})
  [Vz] = zeros(n, mc_t54);            % Vz=(double,[?, 1],{REAL})
  G = 1e-11;                          % G=(double,1.0E-11,[1, 1],{REAL})
  mc_t58 = 1;                         % mc_t58=(double,1.0,[1, 1],{REAL})
  for t = (mc_t58 : dT : T);
    mc_t57 = 1;                         % mc_t57=(double,1.0,[1, 1],{REAL})
    for k = (mc_t57 : n);
      mc_t7 = Rx;                         % mc_t7=(double,[?, 1],{ANY})
      [mc_t8] = Rx(k);                    % mc_t8=(double,[1, 1],{ANY})
      [drx] = minus(mc_t7, mc_t8);        % drx=(double,[?, 1],{ANY})
      mc_t9 = Ry;                         % mc_t9=(double,[?, 1],{ANY})
      [mc_t10] = Ry(k);                   % mc_t10=(double,[1, 1],{ANY})
      [dry] = minus(mc_t9, mc_t10);       % dry=(double,[?, 1],{ANY})
      mc_t11 = Rz;                        % mc_t11=(double,[?, 1],{ANY})
      [mc_t12] = Rz(k);                   % mc_t12=(double,[1, 1],{ANY})
      [drz] = minus(mc_t11, mc_t12);      % drz=(double,[?, 1],{ANY})
      [mc_t15] = times(drx, drx);         % mc_t15=(double,[?, 1],{ANY})
      [mc_t16] = times(dry, dry);         % mc_t16=(double,[?, 1],{ANY})
      [mc_t13] = plus(mc_t15, mc_t16);    % mc_t13=(double,[?, 1],{ANY})
      [mc_t14] = times(drz, drz);         % mc_t14=(double,[?, 1],{ANY})
      [r] = plus(mc_t13, mc_t14);         % r=(double,[?, 1],{ANY})
      mc_t55 = 1.0;                       % mc_t55=(double,1.0,[1, 1],{REAL})
      r(k) = mc_t55;                      % r=(double,[?, 1],{ANY})
      mc_t17 = m;                         % mc_t17=(double,[?, 1],{REAL})
      [mc_t18] = m(k);                    % mc_t18=(double,[1, 1],{REAL})
      [M] = mtimes(mc_t17, mc_t18);       % M=(double,[?, 1],{REAL})
      mc_t56 = 0.0;                       % mc_t56=(double,0.0,[1, 1],{REAL})
      M(k) = mc_t56;                      % M=(double,[?, 1],{REAL})
      mc_t19 = G;                         % mc_t19=(double,1.0E-11,[1, 1],{REAL})
      [mc_t20] = rdivide(M, r);           % mc_t20=(double,[?, 1],{ANY})
      [f] = mtimes(mc_t19, mc_t20);       % f=(double,[1, 1],{ANY})
      [r] = sqrt(r);                      % r=(double,[?, 1],{REAL})
      [drx] = rdivide(drx, r);            % drx=(double,[?, 1],{ANY})
      [dry] = rdivide(dry, r);            % dry=(double,[?, 1],{ANY})
      [drz] = rdivide(drz, r);            % drz=(double,[?, 1],{ANY})
      [frx] = times(f, drx);              % frx=(double,[1, 1],{ANY})
      [fry] = times(f, dry);              % fry=(double,[1, 1],{ANY})
      [frz] = times(f, drz);              % frz=(double,[1, 1],{ANY})
      [mc_t21] = mean(frx);               % mc_t21=(double,[1, 1],{REAL})
      mc_t22 = n;                         % mc_t22=(double,[1, 1],{REAL})
      [mc_t0] = mtimes(mc_t21, mc_t22);   % mc_t0=(double,[1, 1],{REAL})
      Fx(k) = mc_t0;                      % Fx=(double,[?, 1],{REAL})
      [mc_t23] = mean(fry);               % mc_t23=(double,[1, 1],{REAL})
      mc_t24 = n;                         % mc_t24=(double,[1, 1],{REAL})
      [mc_t1] = mtimes(mc_t23, mc_t24);   % mc_t1=(double,[1, 1],{REAL})
      Fy(k) = mc_t1;                      % Fy=(double,[?, 1],{REAL})
      [mc_t25] = mean(frz);               % mc_t25=(double,[1, 1],{REAL})
      mc_t26 = n;                         % mc_t26=(double,[1, 1],{REAL})
      [mc_t2] = mtimes(mc_t25, mc_t26);   % mc_t2=(double,[1, 1],{REAL})
      Fz(k) = mc_t2;                      % Fz=(double,[?, 1],{REAL})
    end
    [ax] = rdivide(Fx, m);              % ax=(double,[?, 1],{ANY})
    [ay] = rdivide(Fy, m);              % ay=(double,[?, 1],{ANY})
    [az] = rdivide(Fz, m);              % az=(double,[?, 1],{ANY})
    mc_t27 = Vx;                        % mc_t27=(double,[?, 1],{ANY})
    [mc_t28] = mtimes(ax, dT);          % mc_t28=(double,[?, 1],{ANY})
    [Vx] = plus(mc_t27, mc_t28);        % Vx=(double,[?, 1],{ANY})
    mc_t29 = Vy;                        % mc_t29=(double,[?, 1],{ANY})
    [mc_t30] = mtimes(ay, dT);          % mc_t30=(double,[?, 1],{ANY})
    [Vy] = plus(mc_t29, mc_t30);        % Vy=(double,[?, 1],{ANY})
    mc_t31 = Vz;                        % mc_t31=(double,[?, 1],{ANY})
    [mc_t32] = mtimes(az, dT);          % mc_t32=(double,[?, 1],{ANY})
    [Vz] = plus(mc_t31, mc_t32);        % Vz=(double,[?, 1],{ANY})
    mc_t33 = Rx;                        % mc_t33=(double,[?, 1],{ANY})
    [mc_t34] = mtimes(Vx, dT);          % mc_t34=(double,[?, 1],{ANY})
    [Rx] = plus(mc_t33, mc_t34);        % Rx=(double,[?, 1],{ANY})
    mc_t35 = Ry;                        % mc_t35=(double,[?, 1],{ANY})
    [mc_t36] = mtimes(Vy, dT);          % mc_t36=(double,[?, 1],{ANY})
    [Ry] = plus(mc_t35, mc_t36);        % Ry=(double,[?, 1],{ANY})
    mc_t37 = Rz;                        % mc_t37=(double,[?, 1],{ANY})
    [mc_t38] = mtimes(Vz, dT);          % mc_t38=(double,[?, 1],{ANY})
    [Rz] = plus(mc_t37, mc_t38);        % Rz=(double,[?, 1],{ANY})
  end
end
% results: [(double,[?, 1],{REAL}), (double,[?, 1],{REAL}), (double,[?, 1],{REAL}), (double,[?, 1],{ANY}), (double,[?, 1],{ANY}), (double,[?, 1],{ANY})]